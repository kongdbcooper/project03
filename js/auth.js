function togglePassword(inputId) {
    const input = document.getElementById(inputId);
    const button = input.nextElementSibling;
    
    if (input.type === 'password') {
        input.type = 'text';
        button.textContent = 'üôà';
    } else {
        input.type = 'password';
        button.textContent = 'üëÅÔ∏è';
    }
}

